package atividade.pkg3.uc9;

public class Consulta {

    private String nomePaciente;
    private String cpfPaciente;
    private String telefonePaciente;
    private String jaEraPaciente;
    private String dataConsulta;
    private String consultaFinalizada;
    private String receita;

    public String getReceita() {
        return receita;
    }

    public void setReceita(String receita) {
        this.receita = receita;
    }

    public String getNomePaciente() {
        return nomePaciente;
    }

    public void setNomePaciente(String nomePaciente) {
        this.nomePaciente = nomePaciente;
    }

    public String getCpfPaciente() {
        return cpfPaciente;
    }

    public void setCpfPaciente(String cpfPaciente) {
        this.cpfPaciente = cpfPaciente;
    }

    public String getTelefonePaciente() {
        return telefonePaciente;
    }

    public void setTelefonePaciente(String telefonePaciente) {
        this.telefonePaciente = telefonePaciente;
    }

    public String getJaEraPaciente() {
        return jaEraPaciente;
    }

    public void setJaEraPaciente(String jaEraPaciente) {
        this.jaEraPaciente = jaEraPaciente;
    }

    public String getDataConsulta() {
        return dataConsulta;
    }

    public void setDataConsulta(String dataConsulta) {
        this.dataConsulta = dataConsulta;
    }

    public String getConsultaFinalizada() {
        return consultaFinalizada;
    }

    public void setConsultaFinalizada(String consultaFinalizada) {
        this.consultaFinalizada = consultaFinalizada;
    }

}
